						<p class="txt--primary_on_light margin--element_top_xs">Now it’s the time to work on the design. The first thing I did was to figure out a way to better display the event log. Event log is essentially a notification system. Usually  the notification button is on the top right corner on the navigation bar for most of the web applications. So I moved it there from the bottom of the screen to match the UI pattern that everyone is familiar with. To solve the readability issue, I decided to move it to the right side panel to reduce the width, which also works perfectly with Striim’s new UI infrastructure.</br></br>

						The next step was to reorganize the layout and add some key metrics required by the clients. The new design still has 2 sections: the overview section and the detail section. In the overview section, I grouped items that have similar properties or visual style. In the detail section, instead of showing 2 identical lists, I only kept the one with the parameters. If one of the items is selected, the timeline chart will show up right next to the list. Users can also toggle between ‘CPU’ and ‘Throughput’ for Apps and ‘CPU’ and ‘Memory’ for Nodes.</p>
