							<p class="txt--primary_on_light margin--element_top_xs">We invited several users to test the health monitor and we observed how they interacted with the product and asked them to think loud during the interview. We got some valuable feedback from both the design perspective and the information architecture perspective. Some users even asked the same questions I had in my previous study.</p>
