						<h1 class="text-left txt--title">Parking + Housing</h1>
						<p class="margin--element_top_sm">Limited spaces with ever growing population in major metropolises like New York significantly lower people's standard of living. One of the main issues with these cities is the poor utilization of certain spaces. Parking garages, for example, are occupied during daytime while emptied at night time. Apartments, which are empty during daytime, suffer from the same usage efficiency problem.</br></br>
						The goal for this design aims to solve this utilization problem by creating a new building type where housing and parking are integrated into one. During daytime, the housing units are compressed to create spaces for parking, while at night, the process reverses with parking turning back into living quarters via a pneumatic structure.</p></br>
